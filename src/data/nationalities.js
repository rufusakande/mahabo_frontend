// Fonction utilitaire pour grouper par continent
export const getNationalitiesByContinent = () => {
  const grouped = {};
  nationalities.forEach(nationality => {
    if (!grouped[nationality.continent]) {
      grouped[nationality.continent] = [];
    }
    grouped[nationality.continent].push(nationality);
  });
  
  // Trier chaque groupe alphabétiquement
  Object.keys(grouped).forEach(continent => {
    grouped[continent].sort((a, b) => a.name.localeCompare(b.name, 'fr'));
  });
  
  return grouped;
};

// Fonction pour rechercher une nationalité
export const searchNationalities = (query) => {
  return nationalities.filter(nationality =>
    nationality.name.toLowerCase().includes(query.toLowerCase())
  );
};